print("\nProgram for Implimenting for loops in python\n")

print("for loop to print number from 1 to 10")
for i in range(1, 11, 1):
	print(i)

print("for loop with one argument only")
print("for loops start with 0 by default and run upto the number less than the no. specified")

for i in range(11):
	print(i)

print("for loop with 2 arguments : initialization and limit, update step is 1 by default")
	
for i in range(10, 21):
	print(i)

print("for loop with 3 arguments")

for i in range(20, 3, -1):
	print(i)